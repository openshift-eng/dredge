def fetch_step_graph(gcs_path, gcsweb_base):
    """Download and parse ci-operator-step-graph.json.
    Returns parsed list of step dicts, or None if not available.
    """
    url = f"{gcsweb_base}{gcs_path}/artifacts/ci-operator-step-graph.json"
    try:
        logger.info(f"Fetching step graph: {url}")
        response = http.session_get(url, timeout=30)
        if response.status_code == 404:
            logger.error("Step graph not found (404)")
            return None
        response.raise_for_status()
        return json.loads(response.text)
    except requests.RequestException as e:
        logger.error(f"Failed to fetch step graph: {e}")
        return None
    except json.JSONDecodeError as e:
        logger.error(f"Failed to parse step graph JSON: {e}")
        return None


def parse_junit_operator_steps(junit_path):
    """
    Parse junit_operator.xml to extract multi-stage test name and inner step names.
    Returns (test_name, [inner_step_names]) or (None, []) if not parseable.

    Test case names follow the pattern:
      "Run multi-stage test {test_name} - {test_name}-{inner_step} container test"
    """
    try:
        tree = ET.parse(junit_path)
    except (ET.ParseError, OSError) as e:
        logger.warning(f"Failed to parse {junit_path}: {e}")
        return None, []

    test_name = None
    inner_steps = []

    for tc in tree.getroot().iter("testcase"):
        name = tc.get("name", "")
        m = re.match(r"Run multi-stage test (\S+) - (\S+) container test", name)
        if not m:
            continue
        step_test_name, full_step = m.group(1), m.group(2)
        if test_name is None:
            test_name = step_test_name
        prefix = step_test_name + "-"
        if full_step.startswith(prefix):
            inner_steps.append(full_step[len(prefix):])
        else:
            inner_steps.append(full_step)

    return test_name, inner_steps


def discover_and_download_junit(gcs_path, gcsweb_base, test_name, inner_steps, junit_dir):
    """
    Find and download JUnit XML files from inner step artifact directories.

    For each inner step, checks two locations:
      1. {step}/artifacts/junit/*.xml  - standard junit output directory
      2. {step}/artifacts/junit*.xml   - junit files at the artifacts root

    Downloads into junit_dir/{inner_step}/.
    """
    total = 0

    for inner_step in inner_steps:
        step_base = f"{gcs_path}/artifacts/{test_name}/{inner_step}/artifacts"
        dest_dir = junit_dir / inner_step
        downloaded = 0

        junit_subdir_url = f"{gcsweb_base}{step_base}/junit/"
        _, files = http.list_gcsweb_dir(junit_subdir_url)
        xml_files = [f for f in files if f.endswith(".xml")]
        for filename in xml_files:
            dest = dest_dir / filename
            url = f"{gcsweb_base}{step_base}/junit/{filename}"
            if http.download_artifact(url, dest):
                downloaded += 1

        artifacts_url = f"{gcsweb_base}{step_base}/"
        _, root_files = http.list_gcsweb_dir(artifacts_url)
        root_xml = [f for f in root_files if f.startswith("junit") and f.endswith(".xml")]
        for filename in root_xml:
            dest = dest_dir / filename
            url = f"{gcsweb_base}{step_base}/{filename}"
            if http.download_artifact(url, dest):
                downloaded += 1

        if downloaded:
            logger.info(f"Downloaded {downloaded} junit file(s) from {inner_step}")
        total += downloaded

    return total


def discover_must_gather(gcs_path, gcsweb_base, step_graph):
    """
    Find must-gather.tar using step graph to identify test steps.
    Returns the full GCS path to must-gather.tar if found, None otherwise.
    """
    test_steps = [s["name"] for s in step_graph if not s.get("name", "").startswith("[")]

    for step_name in test_steps:
        gather_artifacts_url = f"{gcsweb_base}{gcs_path}/artifacts/{step_name}/gather-must-gather/artifacts/"
        _, files = http.list_gcsweb_dir(gather_artifacts_url)
        if "must-gather.tar" in files:
            full_path = f"{gcs_path}/artifacts/{step_name}/gather-must-gather/artifacts/must-gather.tar"
            logger.info(f"Found must-gather at: {step_name}/gather-must-gather/artifacts/must-gather.tar")
            return full_path

    logger.info("No must-gather found in any test step")
    return None


def discover_hypershift_dumps(gcs_path, gcsweb_base, step_graph):
    """
    Find hostedcluster.tar files from steps running the HyperShift test binary.
    Uses step graph to identify relevant steps via [input:hypershift-tests] dependency.
    Returns list of (test_dir_name, gcs_tar_path) tuples, or empty list.
    """
    hypershift_steps = [
        s["name"] for s in step_graph
        if "[input:hypershift-tests]" in (s.get("dependencies") or [])
    ]

    if not hypershift_steps:
        return []

    logger.info(f"Found HyperShift test steps: {hypershift_steps}")
    results = []

    for step_name in hypershift_steps:
        step_url = f"{gcsweb_base}{gcs_path}/artifacts/{step_name}/"
        inner_subdirs, _ = http.list_gcsweb_dir(step_url)

        for inner_subdir in inner_subdirs:
            inner_artifacts_url = f"{gcsweb_base}{gcs_path}/artifacts/{step_name}/{inner_subdir}/artifacts/"
            test_subdirs, _ = http.list_gcsweb_dir(inner_artifacts_url)

            for test_dir in test_subdirs:
                if test_dir.startswith("Test"):
                    tar_path = f"{gcs_path}/artifacts/{step_name}/{inner_subdir}/artifacts/{test_dir}/hostedcluster.tar"
                    results.append((test_dir, tar_path))
                    logger.info(f"Found hostedcluster dump: {step_name}/{inner_subdir}/artifacts/{test_dir}/hostedcluster.tar")

    return results


def extract_tgz(tar_path, dest_dir):
    """Extract tar (gzip compressed) archive."""
    try:
        logger.info(f"Extracting: {tar_path} to {dest_dir}")
        dest_dir.mkdir(parents=True, exist_ok=True)

        try:
            with tarfile.open(tar_path, "r:gz") as tar:
                tar.extractall(path=dest_dir)
        except tarfile.ReadError:
            with tarfile.open(tar_path, "r:") as tar:
                tar.extractall(path=dest_dir)

        logger.info(f"Extracted successfully to: {dest_dir}")
        tar_path.unlink()
        return True
    except (tarfile.TarError, OSError) as e:
        logger.warning(f"Extraction failed: {e}. Keeping tar file for inspection.")
        return False


def write_agents_md(output_dir):
    """
    Write AGENTS.md to the output directory for AI agent context.
    Only overwrites if the existing file was created by this tool.
    """
    agents_md_path = output_dir / "AGENTS.md"

    if agents_md_path.exists():
        try:
            existing_content = agents_md_path.read_text()
            if _AGENTS_MD_SIGNATURE not in existing_content:
                logger.warning(
                    f"AGENTS.md already exists at {agents_md_path} and was not created by this tool. "
                    "Skipping to avoid overwriting user content."
                )
                return False
        except OSError as e:
            logger.warning(f"Could not read existing AGENTS.md: {e}")
            return False

    try:
        content = _AGENTS_MD_CONTENT.format(signature=_AGENTS_MD_SIGNATURE)
        agents_md_path.write_text(content)
        logger.info(f"Wrote AGENTS.md to: {agents_md_path}")
        return True
    except OSError as e:
        logger.warning(f"Failed to write AGENTS.md: {e}")
        return False


def write_build_metadata(build, build_dir, prow_base_url):
    """Write metadata JSON file for a build."""
    refs = build.get("Refs") or {}
    pulls = refs.get("pulls", [])
    spyglass_link = build.get("SpyglassLink", "")

    metadata = {
        "build_id": build.get("ID"),
        "execution_date": build.get("Started"),
        "prow_job_link": f"{prow_base_url}{spyglass_link}" if spyglass_link else None,
        "pr_link": pulls[0].get("link") if pulls else None,
        "commit_link": pulls[0].get("commit_link") if pulls else None,
    }

    metadata_path = build_dir / "build_info.json"
    with open(metadata_path, "w") as f:
        json.dump(metadata, f, indent=2)
    logger.info(f"Wrote metadata to: {metadata_path}")


def process_build(build, output_dir, prow_base_url):
    """Download artifacts for one build."""
    build_id = build.get("ID", "unknown")
    build_dir = output_dir / str(build_id)

    spyglass_link = build.get("SpyglassLink", "")

    if not spyglass_link:
        logger.warning(f"Build {build_id}: No SpyglassLink found")
        return

    logger.info(f"Processing build {build_id}")

    gcs_path = prow.spyglass_to_gcs_path(spyglass_link)
    build_dir.mkdir(parents=True, exist_ok=True)

    gcsweb_base = prow.discover_gcsweb_base(prow_base_url, spyglass_link)
    if not gcsweb_base:
        logger.warning(f"Build {build_id}: Could not discover gcsweb URL, skipping artifact downloads")
        write_build_metadata(build, build_dir, prow_base_url)
        return

    # Download junit_operator.xml
    junit_dest = build_dir / "junit_operator.xml"
    if junit_dest.exists():
        logger.info(f"Build {build_id}: junit_operator.xml already exists, skipping")
    else:
        junit_url = f"{gcsweb_base}{gcs_path}/artifacts/junit_operator.xml"
        if not http.download_artifact(junit_url, junit_dest):
            logger.warning(f"Build {build_id}: junit_operator.xml not available")

    # Discover and download per-step junit files
    junit_dir = build_dir / "junit"
    if junit_dir.exists():
        logger.info(f"Build {build_id}: junit directory already exists, skipping")
    elif junit_dest.exists():
        test_name, inner_steps = parse_junit_operator_steps(junit_dest)
        if test_name and inner_steps:
            count = discover_and_download_junit(gcs_path, gcsweb_base, test_name, inner_steps, junit_dir)
            if count:
                logger.info(f"Build {build_id}: downloaded {count} junit file(s) across steps")
            else:
                logger.info(f"Build {build_id}: no per-step junit files found")
        else:
            logger.info(f"Build {build_id}: no multi-stage test steps found in junit_operator.xml")

    # Fetch step graph for artifact discovery
    step_graph = fetch_step_graph(gcs_path, gcsweb_base)

    if step_graph is None:
        logger.error(f"Build {build_id}: cannot discover artifacts without step graph, skipping")
    else:
        # Discover and download must-gather
        extract_dir = build_dir / "must-gather"
        if extract_dir.exists():
            logger.info(f"Build {build_id}: must-gather already exists, skipping")
        else:
            must_gather_gcs_path = discover_must_gather(gcs_path, gcsweb_base, step_graph)
            if must_gather_gcs_path:
                must_gather_url = f"{gcsweb_base}{must_gather_gcs_path}"
                tar_dest = build_dir / "must-gather.tar"
                if http.download_artifact(must_gather_url, tar_dest):
                    extract_tgz(tar_dest, extract_dir)

        # Discover and download hostedcluster dumps
        hypershift_dir = build_dir / "hypershift-dumps"
        if hypershift_dir.exists():
            logger.info(f"Build {build_id}: hypershift-dumps already exists, skipping")
        else:
            dumps = discover_hypershift_dumps(gcs_path, gcsweb_base, step_graph)
            if dumps:
                for test_name, tar_gcs_path in dumps:
                    tar_url = f"{gcsweb_base}{tar_gcs_path}"
                    tar_dest = build_dir / "hostedcluster.tar"
                    extract_dest = hypershift_dir / test_name
                    if http.download_artifact(tar_url, tar_dest):
                        extract_tgz(tar_dest, extract_dest)

    write_build_metadata(build, build_dir, prow_base_url)

    logger.info(f"Completed processing build {build_id}")
